class ForEach {
    public static void main(String[] args) {
        // Creating an array of integers
        int[] numbers = {15, 27, 38, 43, 59};

        // Using for-each loop to iterate through the array
        System.out.println("The elements of the array are:");
        for (int num : numbers) {
            System.out.println(num);
        }

        // Printing Lab information
        System.out.println("\nQuestion No.: 3d");
        System.out.println("Name: Sishir Dangi");
        System.out.println("Roll No.: 26740");
    }
}
